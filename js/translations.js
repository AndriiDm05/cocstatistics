let currentLang = 'en';

function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'ua' : 'en';
  updateTranslations();
}

function updateTranslations() {
  // Update all elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = translations[currentLang][key] || key;
  });

  // Update button text
  const toggleBtn = document.getElementById('toggleLang');
  if (toggleBtn) toggleBtn.textContent = translations[currentLang].toggleLanguage;

  // Update placeholders
  const playerInput = document.getElementById('playerTag');
  if (playerInput) playerInput.placeholder = translations[currentLang].placeholderPlayer;
  const clanInput = document.getElementById('clanTag');
  if (clanInput) clanInput.placeholder = translations[currentLang].placeholderClan;

  // Update search button
  const searchBtn = document.querySelector('button[onclick="fetchPlayer()"]');
  if (searchBtn) searchBtn.textContent = translations[currentLang].searchBtn;

  // Update clan info button
  const clanInfoBtn = document.querySelector('button[onclick="fetchClan()"]');
  if (clanInfoBtn) clanInfoBtn.textContent = translations[currentLang].clanInfoBtn;

  // Update war and CWL buttons
  const warBtn = document.querySelector('button[onclick="fetchCurrentWar()"]');
  if (warBtn) warBtn.textContent = translations[currentLang].currentWar;
  const cwlBtn = document.querySelector('button[onclick="fetchWarLeague()"]');
  if (cwlBtn) cwlBtn.textContent = translations[currentLang].currentCWL;

  // Update top buttons
  const topPlayersBtn = document.querySelector('button[onclick="showTopPlayers()"]');
  if (topPlayersBtn) topPlayersBtn.textContent = translations[currentLang].topPlayersUA;
  const topClansBtn = document.querySelector('button[onclick="showTopClans()"]');
  if (topClansBtn) topClansBtn.textContent = translations[currentLang].topClansUA;
  const topPlayersGlobalBtn = document.querySelector('button[onclick="showTopPlayersGlobal()"]');
  if (topPlayersGlobalBtn) topPlayersGlobalBtn.textContent = translations[currentLang].topPlayersGlobal;
  const topClansGlobalBtn = document.querySelector('button[onclick="showTopClansGlobal()"]');
  if (topClansGlobalBtn) topClansGlobalBtn.textContent = translations[currentLang].topClansGlobal;
}

window.addEventListener('DOMContentLoaded', updateTranslations);